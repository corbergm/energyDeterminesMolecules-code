            'SizeData'       , 15, ...
            'LineWidth'      , 0.5, ...
            'MArker'         , 'd', ...
            'MarkerFaceColor', BlueColors{i}, ...
            'MarkerFaceAlpha', 1, ...
            'MarkerEdgeColor', 'black'); hold on;
end
