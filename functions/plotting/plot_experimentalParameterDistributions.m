     'Color'              , colors.LightBlue, ...
     'Interpreter'        , 'tex', ...
     'FontSize'           , 10, ...
     'FontName'           , 'Arial', ...
     'Rotation'           , 90, ...
     'Units'              , 'normalized', ...
     'Position'           , [0, (1.5 - 0.4)/(0.1 + size(sources_p, 1))], ...
     'HorizontalAlignment', 'center', ...
     'VerticalAlignment'  , 'bottom'); hold on;
